class Player:
    def __init__(self, data):
        self.name = data["name"]
        self.age = data["age"]
        self.position = data["position"]
        self.team = data["team"]
        
    @classmethod
    def get_team(cls, team_list):
        this_list = []
        for athlete in team_list:
            this_list.append(cls(athlete))
        return this_list
        

